<h2 class="sectionedit2" id="description">Description</h2>
<div class="level2">

<p>
The term is taken to mean the icon image as to be used in different resolutions and color depths of the screen, also with the possibility of overlap, or with animation. The icon can then be both bitmap and vector image.
</p>

<p>
The basis is the declaration which define the resolution, the number and palette of colors and other information to select which option to choose for current rendering. Most of the remaining blocks can be used as an advantage from the formats for bitmap and vector images.
</p>

