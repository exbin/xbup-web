<h2>Design Principles</h2>

<p>Project is using bottom-up approach with layers building on top of each other adding gradually new functionality.</p>

<p>Some of the design principles:</p>

<ul>
<li><strong>Small steps</strong> - Project should be build and evolve by adding small and independent functionality</li>
<li><strong>Universaility first</strong> - Focus should be on support of wide range of use-cases and provide extensibility instead of fixed and compact structure</li>
<li><strong>Abstraction</strong> - Data should be defined using abstraction instead of vague description only</li>
</ul>

