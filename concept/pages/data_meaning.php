<h2 class="sectionedit3" id="transparent_intensional_logic">Transparent Intensional Logic</h2>
<div class="level2">

<p>
Transparent intensional logic is extended logic, which operates over five-dimension vector space, the three classical dimensions of space, time, boolean value and parallel dimensions. This logic allows to define expressions like: If I had a car, I would visit you tomorrow.
</p>

