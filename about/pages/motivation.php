<h1 id="motivation">Motivation</h1>

<p>Project should provide universal protocol as a more feature-rich alternative to currently used binary protocols. It should provide general methods for handling various types of data including:</p>

<ul>
<li><strong>Multimedia files</strong> - Audio, Video, Animation</li>
<li><strong>Filesystem structure</strong> - Allow to represent data in the form of filesystem or as compressed archive</li>
<li><strong>Application API</strong> - Remote or local methods execution supporting parameters and result passing</li>
<li><strong>Serialization protocol</strong> - Provide ability to serialize structured data</li>
<li><strong>Long-term data storage</strong> - Provide way to define data with separate or integrated specification</li>
<li><strong>Huge data</strong> - Use dynamic numeric values to allow support for data in terabytes range or more</li>
<li><strong>Representation of segmented, fragmented or indexed data</strong></li>
</ul>

<p>The protocol is also intended to bring advantages of modern markup languages like XML into binary world. It's a ground-up designed alternative focused on binary data, yet providing similar techniques like XML Schema, XML Namespaces, XSLT and so on.</p>

<p>Where applicable, project should also include concepts from other similar protocols, like for example:</p>

<ul>
<li><strong>RIFF</strong> - Resource Interchange File Format</li>
<li><strong>HDF5/CDF</strong> - Hierarchical Data Format / Common Data Format</li>
<li><strong>ASN.1</strong> - Abstract Syntax Notation One</li>
<li><strong>Matroska/EBML</strong> - Extensible Binary Meta Language</li>
<li><strong>Protocol Buffers</strong> - Serialization protocol for binary data exchange</li>
</ul>

