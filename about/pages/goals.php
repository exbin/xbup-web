<h1 id="goals">Goals</h1>

<p>The primary goal of this project is to create a communication protocol / data format with the following characteristics, ordered by priority:</p>

<ul>
<li><strong>Universal</strong> - Capable of representation of any type of data, suitable for wide use including streaming, long-term storage and parallel accessing</li>
<li><strong>Independent</strong> - Not tightly linked to a particular spoken language, product, company, processing architecture or programming language</li>
<li><strong>Declarative</strong> - Self sufficient for data type definition and with the ability to build data types by combining existing ones</li>
<li><strong>Normative</strong> - Providing reference form for data representation</li>
<li><strong>Flexible</strong> - Support for data transformations, compatibility and extensibility handling</li>
<li><strong>Efficient</strong> - Effective data compacting / compression support for plain binary and structured data</li>
</ul>

