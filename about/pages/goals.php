<h2 id="protocol_design">Protocol Design</h2>
<div>

<p>
Protocol design should cover following areas:
</p>
<ul>
<li><div><strong>Data structure</strong> - How to store/stream data</div>
</li>
<li><div><strong>Data types</strong> - Declaration of data types and build-in types</div>
</li>
<li><div><strong>Data transformations</strong> - Support for data transformations, like compression, encryption and so on</div>
</li>
</ul>

