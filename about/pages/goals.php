<h2 id="prototype_implementation">Prototype Implementation</h2>
<div class="level2">

<p>
Free and open source prototype implementation in Java is currently underway. It should provide following outputs:
</p>
<ul>
<li><div><strong>Basic library</strong> - Library providing support for handling data encoded in the protocol itself</div>
</li>
<li><div><strong>Catalog of types</strong> - Catalog service providing access to declaration of types</div>
</li>
<li><div><strong>Framework</strong> - Local service providing various functions for applications</div>
</li>
<li><div><strong>Editor tool</strong> - Basic tool for viewing and edition</div>
</li>
<li><div><strong>Sample applications and files</strong> - Applications and files for demonstration purposes</div>
</li>
</ul>

