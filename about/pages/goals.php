<h1 id="motivation">Goals</h1>

<div class="level1">

<p>
<a href="../?videos" class="wikilink1">Video presentation</a> about project is recommended for the first time visitors.
</p>

<p>
The primary goal of this project is to create communication protocol/data format with the following characteristics:
</p>
<ul>
<li class="level1"><div class="li"> <strong>Universal</strong> - Suitable for any type of data, persistent storage or streaming purposes</div>
</li>
<li class="level1"><div class="li"> <strong>Independent</strong> - Not tightly linked to particular spoken language or infrastructure/programming language</div>
</li>
<li class="level1"><div class="li"> <strong>Flexible</strong> - Support for data transformations, compatibility and extensibility</div>
</li>
<li class="level1"><div class="li"> <strong>Effective</strong> - Data compactness and compression support for plain binary and structured data</div>
</li>
</ul>

<p>
<a href="?motivation" class="wikilink1">More details about what is project trying to achieve.</a>
</p>

</div>

<h2 class="sectionedit2" id="protocol_design">Protocol Design</h2>
<div class="level2">

<p>
Protocol design should cover following areas:
</p>
<ul>
<li class="level1"><div class="li"> <strong>Data structure</strong> - How to store/stream data</div>
</li>
<li class="level1"><div class="li"> <strong>Data types</strong> - Declaration of data types and build-in types</div>
</li>
<li class="level1"><div class="li"> <strong>Data transformations</strong> - Support for data transformations, like compression, encryption and so on</div>
</li>
</ul>

<p>
<a href="../doc/?protocol" class="wikilink1">More details about protocol&#039;s design.</a>
</p>

</div>

<h2 class="sectionedit3" id="prototype_implementation">Prototype Implementation</h2>
<div class="level2">

<p>
Free and open source prototype implementation in Java is currently underway. It should provide following outputs:
</p>
<ul>
<li class="level1"><div class="li"> <strong>Basic library</strong> - Library providing support for handling data encoded in the protocol itself</div>
</li>
<li class="level1"><div class="li"> <strong>Catalog of types</strong> - Catalog service providing access to declaration of types</div>
</li>
<li class="level1"><div class="li"> <strong>Framework</strong> - Local service providing various functions for applications</div>
</li>
<li class="level1"><div class="li"> <strong>Editor tool</strong> - Basic tool for viewing and edition</div>
</li>
<li class="level1"><div class="li"> <strong>Sample applications and files</strong> - Applications and files for demonstration purposes</div>
</li>
</ul>

<p>
